# 13.Del conjunto de cadenas ['e','f','m','a','ma','j','jl','ag','s','o','n','d']
# muestre a que corresponde a las iniciales de los meses del a√±o, mostrar el
# nombre completo de dichos meses usando sentencia repetitiva y condicional.
l = ['e', 'f', 'm', 'a', 'ma', 'j', 'jl', 'ag', 's', 'o', 'n', 'd']
for i in l:
    if i == 'e':
        print('Enero')
    elif i == 'f':
        print('Febrero')
    elif i == 'm':
        print('Marzo')
    elif i == 'a':
        print('Abril')
    elif i == 'ma':
        print('Mayo')
    elif i == 'j':
        print('Junio')
    elif i == 'jl':
        print('Julio')
    elif i == 'ag':
        print('Agosto')
    elif i == 's':
        print('Septiembre')
    elif i == 'o':
        print('Octubre')
    elif i == 'n':
        print('Noviembre')
    elif i == 'd':
        print('Diciembre')
