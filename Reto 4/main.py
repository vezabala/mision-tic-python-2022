def pasar_minuscula(lista):
    lista_texto_minuscula = []
    texto = ''
    for i in lista:
        texto = texto + ' ' + i
    texto = texto.lower()
    for j in texto.split():
        lista_texto_minuscula.append(j)
    return lista_texto_minuscula


def quitar_signos(lista):
    lista_sin_signo = []
